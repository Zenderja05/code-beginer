// คำสั่งเงื่อนไข if...else
let age = 20;
if (age < 18) {
    console.log("คุณยังไม่บรรลุนิติภาวะ");
} else {
    console.log("คุณบรรลุนิติภาวะแล้ว");
}
// คำสั่งเงื่อนไข switch...case
let day = 3;
switch (day) {
    case 1:
        console.log("วันจันทร์");
        break;
    case 2:
        console.log("วันอังคาร");
        break;
    case 3:
        console.log("วันพุธ");
        break;
    default:
        console.log("วันอื่น ๆ");
}
// ----------------------------------------------------------------------------------------------------------------------------------
// คำสั่ง LOOP
// for LOOP
for (let i = 0; i < 5; i++) {
    console.log("รอบที่ " + (i + 1));
}
// while LOOP
let count = 0;
while (count < 5) {
    console.log("นับ: " + (count + 1));
    count++;
}
// do..while LOOP
let doCount = 0;
do {
    console.log("นับ: " + (doCount + 1));
    doCount++;
} while (doCount < 5);
// ----------------------------------------------------------------------------------------------------------------------------------
// break
for (let i = 0; i < 10; i++) {
    if (i === 5) {
        break; // หยุดลูปเมื่อ i เท่ากับ 5
    }
    console.log("i: " + i);
}
// continue
for (let i = 0; i < 10; i++) {
    if (i % 2 === 0) {
        continue; // ข้ามรอบที่ i เป็นเลขคู่
    }
    console.log("i: " + i);
}
